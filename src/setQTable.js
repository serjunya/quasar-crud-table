export const setColumns = entity => {
    let columns = [];
    entity.properties.forEach((item) => {
        let column = {
            name: item.name,
            label: item.title,
            field: item.name,
            classes: item.className
        }
        switch (item.dataType) {
            case 'Date':
                column.format = val => `${new Date(val).toLocaleString()}`;
                break;
            case 'number':
                column.format = val => `${Number(val || item.defaultValue)}`;
                break;
            default:
                column.align = 'left';
        }
        columns.push(column);
    })
    return columns;
}

